function signUpFunction() {
    var x = document.getElementById("signup");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

function loginFunction() {
    var x = document.getElementById("login");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}